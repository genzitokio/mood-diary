"""Простые рекомендации по тональности записи.

Доп. требование из кейса: "API для анализа тональности текста и рекомендации
(например, предложить расслабляющую музыку при негативных записях)".

Здесь нет внешних API (Spotify/etc), но идея исполнена: на negative выдаём
конкретный совет + ссылку на готовый плейлист, на positive — поощрение,
на neutral — мягкий вопрос для рефлексии. Случайный выбор внутри категории,
чтобы пользователь не видел одно и то же.
"""

from __future__ import annotations

import random


_POSITIVE = [
    "Отличный день! Запомни, что именно сегодня сработало.",
    "Хорошее настроение — лучшее время заняться чем-то новым.",
    "Зафиксируй этот момент: что конкретно тебя порадовало?",
]

_NEUTRAL = [
    "День как день. Может, попробовать что-то непривычное?",
    "Спокойный день — подходящее время для рефлексии.",
    "Что сегодня было самым ярким, пусть даже мелочью?",
]

_NEGATIVE = [
    "Сложный день. Попробуй расслабляющую музыку: https://open.spotify.com/playlist/37i9dQZF1DWZqd5JICZI0u",
    "Когда трудно — короткая прогулка часто помогает. 10-15 минут на свежем воздухе.",
    "Negative-день — нормально. Дыхание 4-7-8 (вдох 4с, задержка 7с, выдох 8с) снижает стресс.",
    "Попробуй сериал/подкаст для разгрузки: https://open.spotify.com/playlist/37i9dQZF1DX4WYpdgoIcn6",
]


def get_recommendation(label: str | None) -> str | None:
    if label == "positive":
        return random.choice(_POSITIVE)
    if label == "negative":
        return random.choice(_NEGATIVE)
    if label == "neutral":
        return random.choice(_NEUTRAL)
    return None
